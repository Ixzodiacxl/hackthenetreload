<h2>Impressum</h2>
<p>
Mit Urteil vom 12. Mai 1998 hat das Landgericht Hamburg entschieden, daß man durch das Anbringen eines Querverweises (Links) die Inhalte der verwiesenen Seite ggf. mit zu verantworten hat. Dies kann nur dadurch verhindert werden, daß man sich ausdrücklich von diesen Inhalten distanziert. Für alle Querverweise auf dieser Seite gilt: Ich betone ausdrücklich, daß ich keinerlei Einfluß auf die Gestaltung und die Inhalte der gelinkten Seiten habe. Deshalb muß ich mich hiermit ausdrücklich von allen Inhalten aller hier zugewiesenen Seiten distanzieren und mache mir ihre Inhalte nicht zu eigen.
</p>

